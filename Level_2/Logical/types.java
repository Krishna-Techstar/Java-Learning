package Level_2.Logical;

public class types {
    public static void main(String[] args){
    System.out.println("This is Logical AND Truth Table ");
    System.out.println( (5>2) && ( 5<10) );
    System.out.println( (5>2) && ( 5>10) );
    System.out.println( (5<2) && ( 5<10) );
    System.out.println( (5<2) && ( 5>10) );
    System.out.println("This is Logical OR Truth Table ");
    System.out.println( (5>2) || ( 5<10) );
    System.out.println( (5>2) || ( 5>10) );
    System.out.println( (5<2) || ( 5<10) );
    System.out.println( (5<2) || ( 5>10) );
    System.out.println("This is Logical AND Truth Table ");
    System.out.println( !(5>2)  );
    System.out.println( !(5<2));

    }
} 
