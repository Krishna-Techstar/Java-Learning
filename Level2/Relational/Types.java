package Level2.Relational;

public class Types {
    public static void main(String[] args){
        int a = 25;
        int b = 45;
        int c = 25;
        System.out.println((a==c));
        System.out.println((a!=b));
        System.out.println((a<c));
        System.out.println((a>b));
        System.out.println((a>=c));
        System.out.println((a<=c));
    }
}
