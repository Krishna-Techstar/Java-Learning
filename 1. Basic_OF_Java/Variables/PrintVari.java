package Basic_OF_Java.Variables;

public class PrintVari {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        int c=25;
        String name="Tony Stark";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(name);

        a=b;
        System.out.println(a);
    }
}
